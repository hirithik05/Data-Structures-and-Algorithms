package java_tutorial;

public class Program9 {
	public static void main(String[] args) {
		int count = 4;
		switch(count) {
		case 1:
			System.out.println("ONE");
			break;
		case 2:
			System.out.println("TWO");
			break;
		case 3:
			System.out.println("THREE");
			break;
		default:
			System.out.println("Default Case");
		}
	}

}
